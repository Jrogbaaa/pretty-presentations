const generateSquares = (data: typeof squareData) => {
  return data.map((sq) => (
    <motion.div
      key={sq.id}
      layout
      transition={{ duration: 1.5, type: "spring" }}
      className="w-full h-full rounded-md overflow-hidden bg-gray-200 relative"
    >
      <Image
        src={sq.src}
        alt={sq.alt}
        fill
        sizes="(max-width: 768px) 25vw, 12.5vw"
        className="object-cover"
        priority={sq.id <= 4}
      />
    </motion.div>
  ));
};

const ShuffleGrid = () => {
  const timeoutRef = useRef<NodeJS.Timeout | null>(null);
  const [squares, setSquares] = useState(() => generateSquares(squareData));

  const shuffleSquares = () => {
    setSquares(generateSquares(shuffle(squareData)));

    timeoutRef.current = setTimeout(shuffleSquares, 3000);
  };

  useEffect(() => {
    // Start shuffling only after mount (client-side only)
    shuffleSquares();

    return () => {
      if (timeoutRef.current) {
        clearTimeout(timeoutRef.current);
      }
    };
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, []);

  return (
    <div className="grid grid-cols-4 grid-rows-4 h-[450px] gap-1">
      {squares.map((sq) => sq)}
    </div>
  );
};
